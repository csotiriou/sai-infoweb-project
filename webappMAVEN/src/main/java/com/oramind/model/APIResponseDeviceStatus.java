package com.oramind.model;

import com.google.gson.JsonElement;
import com.google.gson.JsonObject;

public class APIResponseDeviceStatus extends APIResponse{
	boolean registered;
	String macAddress;
	String apiKey;
	
	
	@Override
	public JsonElement toJSONElement() {
		JsonObject object = new JsonObject();
		object.addProperty("isRegistered", Boolean.valueOf(registered));
		object.addProperty("mac", macAddress);
		object.addProperty("apiKey", apiKey);
		return object;
	}
	
	public boolean isRegistered() {
		return registered;
	}
	public void setRegistered(boolean registered) {
		this.registered = registered;
	}
	public String getMacAddress() {
		return macAddress;
	}
	public void setMacAddress(String macAddress) {
		this.macAddress = macAddress;
	}
	public String getApiKey() {
		return apiKey;
	}
	public void setApiKey(String apiKey) {
		this.apiKey = apiKey;
	}
	
}
